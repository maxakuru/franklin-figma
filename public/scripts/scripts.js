function loadTheme(conf = config) {
  const theme = getMetadata('theme');
  const isLoaded = () => {
    document.body.classList.add('is-Loaded');
  };
  if (theme) {
    const themeConf = conf.themes[theme] || {};
    if (themeConf.class) {
      document.body.classList.add(themeConf.class);
    }
    if (themeConf.styles) {
      loadCSS(`${themeConf.location}${themeConf.styles}`, isLoaded);
    } else {
      isLoaded();
    }
  } else {
    isLoaded();
  }
}

