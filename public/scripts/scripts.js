export function makeRelative(anchor) {
  const { href } = anchor;
  const url = new URL(href);
  const host = url.hostname;
  if (host.endsWith(`${PROJECT}.hlx3.page`) || host.endsWith(`${PROJECT}.hlx.live`)) {
    const relative = `${url.pathname}${url.search}${url.hash}`;
    anchor.setAttribute('href', relative);
    return relative;
  }
  // external link
  anchor.target = '_blank';
  return href;
}

export function setSVG(anchor) {
  const { textContent } = anchor;
  const href = anchor.getAttribute('href');
  const ext = textContent.substr(textContent.lastIndexOf('.') + 1);
  if (ext !== 'svg') return;
  const img = document.createElement('img');
  img.src = textContent;
  if (textContent === href) {
    anchor.insertAdjacentElement('afterend', img);
    anchor.remove();
  } else {
    anchor.textContent = '';
    anchor.append(img);
  }
}

